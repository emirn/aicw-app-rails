/**
 * Convert a timezone string to a Timezone enum value
 * @param str - Timezone string (e.g., "America/New_York", "Europe/London")
 * @returns Timezone enum value or null if not found
 * @example
 * stringToTimezone("America/New_York") // returns Timezone.AMERICA_NEW_YORK
 * stringToTimezone("Invalid/Timezone") // returns null
 */
export function stringToTimezone(str: string | null | undefined): Timezone | null {
  if (!str) return null;
  const normalized = str.trim().toLowerCase();

  // Special case: "UTC" maps to "Etc/UTC" (IANA standard)
  if (normalized === "utc"){
    return UTC_TIMEZONE;
  }

  return STRING_TO_ENUM_MAP[normalized] ?? null;
}

